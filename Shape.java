class Shape {
	private int width;
	private int height;

	public Shape(int w, int h) {
		this.width=w;
		this.height=h;
	}

	public int Area(int w, int h) {
		return ((w*h)/2);
	}

	public double Area(double w, double h){
		return (w*h);
	}

	public static void main(String ar[]) {
		Shape s1=new Shape(3, 5);
		Shape s2=new Shape(6, 8);

		System.out.println(s1.Area(7, 9));
		System.out.println(s2.Area(6.9, 3.6));
	}
}