class Wine{
	private String manufacturer;
	private String name;
	private String country;
	private String region;
	private String kind;
	private int year;
	private int grade;

	public Wine(String m, String n, String c, String r, String k, int y, int g){
		this.manufacturer=m;
		this.name=n;
		this.country=c;
		this.region=r;
		this.kind=k;
		this.year=y;
		this.grade=g;
	}

	public Wine(String m, String n){
		this.manufacturer=m;
		this.name=n;
	}
}