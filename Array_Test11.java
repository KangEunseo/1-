class Array_Test11{
	public static void main(String ar[]){
		int n[]=new int[900];
		int count=0, sum=0;

		for (int i=0; i<n.length; i++) {
			n[i]=i+100;
			if (n[i]%4!=0) {
				count+=1;
				sum+=n[i];
			}
		}

		System.out.println("100부터 999까지 수 중 4의 배수가 아닌 수의 개수 : "+count+"(개)");
		System.out.println("100부터 999까지 수 중 4의 배수가 아닌 수의 합 : "+sum);
	}
}